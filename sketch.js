
function setup() {
  createCanvas(200, 200);
}

function draw() {
  background(220);
  
  
  fill(255); 
  ellipse(100, 100, 80, 80); 
  
  
  fill(0); 
  ellipse(80, 90, 10, 10); 
  ellipse(120, 90, 10, 10); 


  
  noFill(); 
  stroke(0); 
  strokeWeight(2); 
  line(90, 120, 110, 120); 
  
  fill (0)
  line (100, 110, 100, 100)
  
}